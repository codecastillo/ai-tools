export function findGlossaryTerm(termOrSlug: string): GlossaryTerm | null {
  const needle = termOrSlug.trim().toLowerCase();
  return (
    GLOSSARY.find((g) => g.slug === needle) ??
    GLOSSARY.find((g) => g.term.toLowerCase() === needle) ??
    null
  );
}
